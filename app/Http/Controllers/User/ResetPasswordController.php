     */
    public function update(Request $request, $id)
    {
        if ($request->isMethod('PUT')) {
            $this->validate($request, [
                'oldpassword' => 'required|',
                'newpassword' => 'required|',
                'confirmpassword' => 'required_with:newpassword|same:newpassword|'
            ]);
            $users= User::find($id);
           //
            if (Hash::check($request->input('oldpassword') , $users->password)) {
                //dd(Hash::make($request->input('newpassword')));
                $users->password = Hash::make($request->input('newpassword')) ;
                $users->save();
            }


        }
        return redirect('/users/profile')->with('status' , 'updated password');

    }

